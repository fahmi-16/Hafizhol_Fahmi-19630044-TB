    private void BTubah1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTubah1ActionPerformed
        pesan.setId_keberangkatan(id_berangkatL.getText());
        pesan.setNama(namaTF.getText());
        pesan.setAlamat(alamatTF.getText());
        pesan.setTelepon(phoneTF.getText());
        
        if (pesan.update()){
            tampilData();
            JOptionPane.showMessageDialog(null, "Berhasil Di Ubah");
            reset();
        }else{
            JOptionPane.showMessageDialog(null, "Gagal Di Ubah");
        }
    }//GEN-LAST:event_BTubah1ActionPerformed

    private void BThapus1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BThapus1ActionPerformed
        try {
            int jawab;

            if ((jawab = JOptionPane.showConfirmDialog(null, "Ingin menghapus data?", "konfirmasi", JOptionPane.YES_NO_OPTION)) == 0) {
                pesan.setId_keberangkatan(id_berangkatL.getText());

                if (pesan.delete()){
                    tampilData();
                    JOptionPane.showMessageDialog(null, "Berhasil Di Hapus");
                    reset();
                }else{
                    JOptionPane.showMessageDialog(null, "Gagal Di Hapus");
                }

                tampilData();
                reset();
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_BThapus1ActionPerformed

