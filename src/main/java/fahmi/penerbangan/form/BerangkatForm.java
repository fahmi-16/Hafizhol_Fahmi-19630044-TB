    public void tampilData(){
        ArrayList<Pesan> list = pesan.read();
        
        DefaultTableModel tableModel = (DefaultTableModel) tbBerangkat.getModel();
        tableModel.setRowCount(0);
        
        Object[] row = new Object[6];
        
        for (int i = 0; i<list.size(); i++){
            row[0] = list.get(i).getId_keberangkatan();
            row[1] = list.get(i).getId_penerbangan();
            row[2] = list.get(i).getId_kelas();
            row[3] = list.get(i).getNama();
            row[4] = list.get(i).getAlamat();
            row[5] = list.get(i).getTelepon();

            tableModel.addRow(row);
        }
    }
    
    public void tampilCari(){
        ArrayList<Pesan> list = pesan.readBerangkat(cariTF.getText());
        
        DefaultTableModel tableModel = (DefaultTableModel) tbBerangkat.getModel();
        tableModel.setRowCount(0);
        
        Object[] row = new Object[6];
        
        if(list.size() > 0){
//            System.out.println("Jalan");
            for (int i = 0; i<list.size(); i++){
                row[0] = list.get(i).getId_keberangkatan();
                row[1] = list.get(i).getId_penerbangan();
                row[2] = list.get(i).getId_kelas();
                row[3] = list.get(i).getNama();
                row[4] = list.get(i).getAlamat();
                row[5] = list.get(i).getTelepon();

                tableModel.addRow(row);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Tidak Ada Data");
        }
    }
    
