    private void BTpesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTpesanActionPerformed
        transaksi();
        int confirm = JOptionPane.showConfirmDialog(this,
            "Lakukan pembayaran untuk konfirmasi pemesanan Tiket",
            "Konfirmasi Pemesanan",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE);
        if (confirm == JOptionPane.YES_OPTION) {
            
            if(namaTF.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Masukan Nama");
            }else if (alamatTF.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Masukan Alamat");
            }else if (telpTF.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Masukan Telepon");
            }else if (bayarTF.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Masukan Nominal Pembayaran");
            }else{
                pesan.setHarga(hargaL.getText());
                pesan.setBayar(bayarTF.getText());
                pesan.setKembali(kembaliL.getText());
                pesan.setTanggal(TanggalSekarangL.getText());
                
                pesan.setId_keberangkatan(id_berangkatL.getText());
                pesan.setId_penerbangan(no_penerbanganL.getText());
                pesan.setId_kelas(id_kelas);
                pesan.setNama(namaTF.getText());
                pesan.setAlamat(alamatTF.getText());
                pesan.setTelepon(telpTF.getText());

                if (pesan.create()){
                JOptionPane.showMessageDialog(null, "Berhasil Di Simpan");
                new CariPenerbanganForm().setVisible(true);
                dispose();
                }else{
                    JOptionPane.showMessageDialog(null, "Gagal Di Simpan");
                }

            }
            
        }
    }//GEN-LAST:event_BTpesanActionPerformed

