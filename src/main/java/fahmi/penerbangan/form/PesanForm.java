    public void tampilDataKelas(){
        ArrayList<Kelas> list = kelas.readKelas(id_kelas);
        
        for (int i = 0; i<list.size(); i++){
            no_penerbanganL.setText(list.get(i).getId_penerbangan());
            kelasL.setText(list.get(i).getKelas());
            bagasiL.setText(String.valueOf(list.get(i).getBagasi()));
            hargaL.setText(String.valueOf(list.get(i).getHarga()));
        }
        
        kode_berangkat();
    }
    
    public void kode_berangkat(){
        ArrayList<Pesan> list = pesan.readBerangkat(no_penerbanganL.getText());
        if(list.size() > 0){
            for (int i = 0; i<list.size(); i++){
                String kodeAsli = list.get(i).getId_keberangkatan();
                int kodeAngka =(Integer.parseInt(kodeAsli.substring(9))+1);
    //                System.out.println(kodeAngka);
                id_berangkatL.setText(String.valueOf(id_kelas+"--B"+kodeAngka));
                }
        }else if(list.size() == 0){
            id_berangkatL.setText(id_kelas+"--B1");
        }
    }
    
    public void tanggalSekarang() {
        Date date = new Date();
        DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd"); 
        TanggalSekarangL.setText(dateFormat.format(date));
    }
    
    public void tampilDataPenerbangan(){
        ArrayList<Penerbangan> list = penerbangan.readPenerbangan(no_penerbanganL.getText());
        for (int i = 0; i<list.size(); i++){
            maskapaiL.setText(list.get(i).getMaskapai());
            no_pesawatL.setText(list.get(i).getNo_pesawat());
            nama_pesawatL.setText(list.get(i).getNama_pesawat());
            tanggalL.setText(list.get(i).getTanggal()+" / "+list.get(i).getJam());
            setTanggal(list.get(i).getTanggal());
            setJam(list.get(i).getJam());
            asalL.setText(list.get(i).getAsal()+" - "+list.get(i).getTujuan());
            setAwal(list.get(i).getAsal());
            setTujuan(list.get(i).getTujuan());
        }
    }
    
    public void transaksi(){
        double harga, bayar, kembali;
        harga = Double.parseDouble(hargaL.getText());
        bayar = Double.parseDouble(bayarTF.getText());
        kembali =  bayar - harga;
        kembaliL.setText(String.valueOf(kembali));
    }
    
