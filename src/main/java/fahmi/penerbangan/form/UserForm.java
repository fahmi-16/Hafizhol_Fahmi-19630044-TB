    private void tbUserMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbUserMouseClicked
        userTF.setText(tbUser.getValueAt(tbUser.getSelectedRow(), 0).toString());
        passTF.setText(tbUser.getValueAt(tbUser.getSelectedRow(), 1).toString());
        levelCB.setSelectedItem(tbUser.getValueAt(tbUser.getSelectedRow(), 2).toString());
        userTF.setEnabled(false);
        BTtambah.setEnabled(false);
        BTubah.setEnabled(true);
        BThapus.setEnabled(true);
    }//GEN-LAST:event_tbUserMouseClicked

    private void BTtambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTtambahActionPerformed
        user.setUser(userTF.getText());
        user.setPass(passTF.getText());
        user.setLevel((String) levelCB.getSelectedItem());

        if (user.create()){
            tampilData();
            JOptionPane.showMessageDialog(null, "Berhasil Di Simpan");
            reset();
        }else{
            JOptionPane.showMessageDialog(null, "Gagal Di Simpan");
        }
    }//GEN-LAST:event_BTtambahActionPerformed

