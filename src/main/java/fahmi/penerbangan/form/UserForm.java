    public void tampilData(){
        ArrayList<User> list = user.read();
        
        DefaultTableModel tableModel = (DefaultTableModel) tbUser.getModel();
        tableModel.setRowCount(0);
        
        Object[] row = new Object[3];
        
        if(list.size() > 0){
//            System.out.println("Jalan");
            for (int i = 0; i<list.size(); i++){
                row[0] = list.get(i).getUser();
                row[1] = list.get(i).getPass();
                row[2] = list.get(i).getLevel();
                
                tableModel.addRow(row);
            }
        }
    }
    
    public void tampilCari(){
        ArrayList<User> list = user.search(cariTF.getText());
        
        DefaultTableModel tableModel = (DefaultTableModel) tbUser.getModel();
        tableModel.setRowCount(0);
        
        Object[] row = new Object[5];
        
        if(list.size() > 0){
//            System.out.println("Jalan");
            for (int i = 0; i<list.size(); i++){
                row[0] = list.get(i).getUser();
                row[1] = list.get(i).getPass();
                row[2] = list.get(i).getLevel();
                
                tableModel.addRow(row);
            }
        }
    }
    
    public void reset() {
        userTF.setEnabled(true);
        userTF.setText("");
        passTF.setText("");
//        levelCB.setSelectedItem("");
        levelCB.setSelectedIndex(0);
        BTtambah.setEnabled(true);
        BTubah.setEnabled(false);
        BThapus.setEnabled(false);
        tampilData();
      }
